def main():
    t0 = time.time()

    if len(X_train) < 5:
        print("ERROR: Not enough training data. Run prepare.py first.")
        print("RESULT: PRED_SCORE=0.0000")
        return

    # ── Poly-ridge: λ selected via LODO-CV on training data (n=60 holdout) ──
    # NOTE: λ is chosen using only n=40,50 → n=60 CV, never touching extrap.
    # This section pre-computes the CV split needed for poly λ selection.
    X_train_poly = poly_features(X_train)
    X_interp_poly = poly_features(X_interp)
    X_extrap_poly = poly_features(X_extrap) if len(X_extrap) > 0 else X_extrap

    cv_dim_pre = max(config["dimensions"])
    cv_tr_mask_pre = np.array([meta_all[train_idx[i]]["n"] != cv_dim_pre
                                for i in range(len(X_train))])
    cv_val_mask_pre = ~cv_tr_mask_pre
    X_cv_tr_pre_poly = poly_features(X_train[cv_tr_mask_pre])
    X_cv_val_pre_poly = poly_features(X_train[cv_val_mask_pre])
    y_cv_tr_pre = y_train[cv_tr_mask_pre]
    y_cv_val_pre = y_train[cv_val_mask_pre]
    # Also get theory info on CV val for tc-aware selection
    cv_val_meta_pre = [meta_all[train_idx[i]] for i in range(len(X_train)) if cv_val_mask_pre[i]]
    cv_primal_mask_pre = np.array([m["attack"] == "primal" for m in cv_val_meta_pre])
    cv_theory_primal_pre = np.array([m["theory_classical_log2"] for m in cv_val_meta_pre
                                      if m["attack"] == "primal"])

    best_poly_cv_score = -1
    best_poly_lam = 1.0
    for lam in [1e-4, 1e-3, 1e-2, 0.1, 0.5, 1.0, 5.0, 10.0]:
        w = fit_linear_model(X_cv_tr_pre_poly, y_cv_tr_pre, lam=lam)
        y_cv_val_pred = predict_linear(X_cv_val_pre_poly, w)
        r2_cv = max(0.0, r_squared(y_cv_val_pre, y_cv_val_pred))
        tc_cv = max(0.0, spearman_rank_corr(cv_theory_primal_pre,
                    y_cv_val_pred[cv_primal_mask_pre])) \
                if cv_primal_mask_pre.any() and len(cv_theory_primal_pre) > 2 else 0.0
        cv_s = 0.6 * r2_cv + 0.4 * tc_cv
        if cv_s > best_poly_cv_score:
            best_poly_cv_score = cv_s
            best_poly_lam = lam

    # Retrain poly on ALL training data with CV-selected λ
    best_poly_w = fit_linear_model(X_train_poly, y_train, lam=best_poly_lam)
    y_ip_poly = predict_linear(X_interp_poly, best_poly_w)
    y_ep_poly = predict_linear(X_extrap_poly, best_poly_w) if len(X_extrap) > 0 else np.array([])
    y_ep_p_poly = y_ep_poly[extrap_primal_local_idx] if len(y_ep_poly) > 0 else np.array([])
    best_poly_score, r2i_poly, r2e_poly, tc_poly = compute_pred_score(
        y_interp, y_ip_poly, y_extrap, y_ep_poly,
        theory_preds_extrap_primal, y_ep_p_poly)
    print(f"  Poly-ridge (LODO-CV λ={best_poly_lam}): SCORE={best_poly_score:.4f} "
          f"(R²i={r2i_poly:.4f}, R²e={r2e_poly:.4f}, tc={tc_poly:.4f})")

    # ── Neural: tune rank_lambda via leave-one-dimension-out CV on training data ──
    # Use n=60 dimension as CV holdout; train on n=40,50; never touch extrap (n=70,80).
    # ── LODO-CV: joint (λ, β_poly) tuning — never touch extrap ──
    # Train on n=40,50; validate on n=60 (within training dims).
    # β_poly blends poly-ridge (high R²) with ranking-neural (high theory_corr).
    print("\n--- LODO-CV: joint (λ, β_poly) sweep (holdout dim=60) ---")
    cv_dim = max(config["dimensions"])
    cv_train_mask = np.array([meta_all[train_idx[i]]["n"] != cv_dim
                               for i in range(len(X_train))])
    cv_val_mask = ~cv_train_mask
    X_cv_tr, y_cv_tr = X_train[cv_train_mask], y_train[cv_train_mask]
    X_cv_val, y_cv_val = X_train[cv_val_mask], y_train[cv_val_mask]
    cv_val_meta = [meta_all[train_idx[i]] for i in range(len(X_train)) if cv_val_mask[i]]
    cv_primal_mask = np.array([m["attack"] == "primal" for m in cv_val_meta])
    cv_theory_primal = np.array([m["theory_classical_log2"] for m in cv_val_meta
                                  if m["attack"] == "primal"])
    theory_cv_tr = np.array([meta_all[train_idx[i]]["theory_classical_log2"]
                              for i in range(len(X_train)) if cv_train_mask[i]])
    primal_cv_tr = np.array([meta_all[train_idx[i]]["attack"] == "primal"
                              for i in range(len(X_train)) if cv_train_mask[i]])

    # Poly-ridge on n=40,50 CV fold
    X_cv_tr_poly = poly_features(X_cv_tr)
    X_cv_val_poly = poly_features(X_cv_val)
    best_cv_poly_w = None
    _best_cv_poly_s = -1
    for lam in [1e-4, 1e-3, 1e-2, 0.1, 1.0]:
        w = fit_linear_model(X_cv_tr_poly, y_cv_tr, lam=lam)
        s = r_squared(y_cv_val, predict_linear(X_cv_val_poly, w))
        if s > _best_cv_poly_s:
            _best_cv_poly_s = s
            best_cv_poly_w = w
    y_cv_poly = predict_linear(X_cv_val_poly, best_cv_poly_w)

    # ── OOF score function (used for family selection) ──
    def oof_score(y_pred_cv):
        """OOF CV score: 0.6*R²_cv + 0.4*theory_corr_cv (out-of-fold only)."""
        r2 = max(0.0, r_squared(y_cv_val, y_pred_cv))
        tc_v = max(0.0, spearman_rank_corr(cv_theory_primal, y_pred_cv[cv_primal_mask])) \
               if cv_primal_mask.any() and len(cv_theory_primal) > 2 else 0.0
        return 0.6 * r2 + 0.4 * tc_v

    # Poly-only OOF score (model trained on n=40,50, predictions on n=60 — true OOF)
    poly_oof = oof_score(y_cv_poly)

    # Best blend+neural OOF scores — tracked DURING the loop (models trained on n=40,50)
    best_cv_score = -1
    best_lambda_cv = 0.8
    best_beta_cv = 0.2
    best_nn_oof = -1      # best neural-only OOF (beta_poly=0.0)
    best_nn_oof_lam = 0.8

    for rank_lam in [0.5, 0.7, 0.8, 0.9, 1.0]:
        # Single CV model (simpler, consistent with earlier iters)
        torch.manual_seed(SEED)
        pred_cv, _, _, _ = fit_neural_model(
            X_cv_tr, y_cv_tr, X_cv_val, y_cv_val,
            epochs=600, lr=5e-4, hidden=(128, 64, 32),
            rank_lambda=rank_lam, theory_tr=theory_cv_tr, primal_mask_tr=primal_cv_tr
        )
        y_cv_nn = pred_cv(X_cv_val)
        for beta_poly in [0.0, 0.1, 0.15, 0.2, 0.25, 0.3, 0.4, 0.5]:
            y_blend_cv = beta_poly * y_cv_poly + (1 - beta_poly) * y_cv_nn
            cv_score = oof_score(y_blend_cv)
            if cv_score > best_cv_score:
                best_cv_score = cv_score
                best_lambda_cv = rank_lam
                best_beta_cv = beta_poly
            # Track pure-neural OOF (beta_poly=0.0 → y_blend_cv = y_cv_nn)
            if beta_poly == 0.0 and cv_score > best_nn_oof:
                best_nn_oof = cv_score
                best_nn_oof_lam = rank_lam

    # ── OOF-based family selection ──
    # All three scores computed on n=60 holdout, models trained on n=40,50 — fully OOF.
    family_oof = {"poly": poly_oof, "neural": best_nn_oof, "blend": best_cv_score}
    winner_family = max(family_oof, key=family_oof.get)
    print(f"  OOF family scores: poly={poly_oof:.4f}  neural={best_nn_oof:.4f}  "
          f"blend={best_cv_score:.4f}")

    if winner_family == "neural":
        best_lambda_cv = best_nn_oof_lam
        best_beta_cv = 0.0
    elif winner_family == "poly":
        best_beta_cv = 1.0   # pure poly; best_lambda_cv unused for neural

    print(f"  → Winner by OOF: {winner_family} "
          f"(λ={best_lambda_cv if winner_family != 'poly' else 'N/A'}, "
          f"β_poly={best_beta_cv})")

    # ── Final fit on ALL training data with winner's config ──
    # Neural early stopping uses X_cv_val (training-dim fold) — not X_interp.
    # X_interp is reserved as a true held-out test set.
    theory_train = np.array([meta_all[train_idx[i]]["theory_classical_log2"]
                              for i in range(len(X_train))])
    primal_mask_train = np.array([meta_all[train_idx[i]]["attack"] == "primal"
                                   for i in range(len(X_train))])

    if winner_family != "poly":
        nn_predict_fns = []
        for seed_offset in range(5):   # 5-model ensemble for variance reduction
            torch.manual_seed(SEED + seed_offset)
            pred_fn, _, _, _ = fit_neural_model(
                X_train, y_train, X_cv_val, y_cv_val,
                epochs=800, lr=5e-4, hidden=(128, 64, 32),
                rank_lambda=best_lambda_cv,
                theory_tr=theory_train, primal_mask_tr=primal_mask_train
            )
            nn_predict_fns.append(pred_fn)

        def predict_nn(X):
            return np.mean([f(X) for f in nn_predict_fns], axis=0)

        y_ip_nn = predict_nn(X_interp)
        y_ep_nn = predict_nn(X_extrap) if len(X_extrap) > 0 else np.array([])
    else:
        y_ip_nn = y_ep_nn = np.array([])
        predict_nn = lambda X: np.zeros(len(X))

    # Build final predictions for winner family
    if winner_family == "poly":
        y_interp_pred = y_ip_poly
        y_extrap_pred = y_ep_poly
        best_predict = lambda X: predict_linear(poly_features(X), best_poly_w)
        best_label = "poly"
    elif winner_family == "neural":
        y_interp_pred = y_ip_nn
        y_extrap_pred = y_ep_nn
        best_predict = predict_nn
        best_label = f"neural(λ={best_lambda_cv})"
    else:  # blend
        y_interp_pred = best_beta_cv * y_ip_poly + (1 - best_beta_cv) * y_ip_nn
        y_extrap_pred = best_beta_cv * y_ep_poly + (1 - best_beta_cv) * y_ep_nn \
                        if len(y_ep_nn) > 0 else np.array([])
        best_predict = lambda X, _bw=best_poly_w, _bc=best_beta_cv, _nn=predict_nn: \
            _bc * predict_linear(poly_features(X), _bw) + (1 - _bc) * _nn(X)
        best_label = f"blend(λ={best_lambda_cv},β={best_beta_cv})"

    # ── Single held-out evaluation: PRED_SCORE on X_interp + X_extrap ──
    y_ep_p = y_extrap_pred[extrap_primal_local_idx] if len(y_extrap_pred) > 0 else np.array([])
    best_score, r2i, r2e, tc = compute_pred_score(
        y_interp, y_interp_pred, y_extrap, y_extrap_pred,
        theory_preds_extrap_primal, y_ep_p)
    print(f"\n  Best model: {best_label} (SCORE={best_score:.4f})")

    # ── NIST extrapolation ──
    extrapolate_to_nist(best_predict, nist_params, rows)

    # ── Discrepancy analysis ──
    print("\n" + "=" * 70)
    print("EMPIRICAL vs THEORETICAL DISCREPANCY")
    print("=" * 70)
    if len(X_extrap) > 0:
        for i, idx in enumerate(extrap_idx):
            m = meta_all[idx]
            print(f"  n={m['n']}  β={m['beta']:3d} ({m['attack']:6s}):  "
                  f"actual={y_extrap[i]:+7.2f}  model={y_extrap_pred[i]:+7.2f}  "
                  f"CoreSVP={m['theory_classical_log2']:+7.2f}  "
                  f"model_err={y_extrap_pred[i]-y_extrap[i]:+.2f}  "
                  f"theory_gap={y_extrap[i]-m['theory_classical_log2']:+.2f}")
            if i > 25:
                print(f"  ... ({len(extrap_idx) - i - 1} more rows)")
                break

    # ── Record & save ──
    meta_interp = [meta_all[i] for i in interp_test_idx]
    meta_extrap_list = [meta_all[i] for i in extrap_idx]
    history = record_iteration(best_score, r2i, r2e, tc, label="iter24")
    save_progress(y_interp, y_interp_pred, y_extrap, y_extrap_pred,
                  best_score, history,
                  meta_interp=meta_interp, meta_extrap=meta_extrap_list)

    elapsed = time.time() - t0
    print(f"\nCompleted in {elapsed:.1f}s")
    print(f"RESULT: PRED_SCORE={best_score:.4f}")


if __name__ == "__main__":
    main()
