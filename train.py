# Baseline Model: Small Neural Network
# ─────────────────────────────────────────────
class CostMLP(nn.Module):
    def __init__(self, input_dim, hidden=(64, 32)):
        super().__init__()
        layers = []
        d_in = input_dim
        for h in hidden:
            layers += [nn.Linear(d_in, h), nn.ReLU(), nn.Dropout(0.1)]
            d_in = h
        layers.append(nn.Linear(d_in, 1))
        self.net = nn.Sequential(*layers)

    def forward(self, x):
        return self.net(x).squeeze(-1)


def pairwise_ranking_loss(preds, theory_vals, margin=0.1):
    """
    Pairwise ranking hinge loss for theory-consistent predictions.
    Penalizes pairs (i,j) where theory_i > theory_j but pred_i < pred_j + margin.
    Applied to primal training rows only; vectorized O(N²).
    """
    pred_diff = preds.unsqueeze(0) - preds.unsqueeze(1)           # [N, N]: pred_i - pred_j
    theory_diff = theory_vals.unsqueeze(0) - theory_vals.unsqueeze(1)  # [N, N]
    # Only penalize pairs with meaningfully different theory values (gap > 1.0 log2-bit)
    violation_mask = (theory_diff > 1.0).float()
    if violation_mask.sum() == 0:
        return torch.tensor(0.0, device=preds.device)
    violations = torch.relu(margin - pred_diff) * violation_mask
    return violations.sum() / violation_mask.sum()


def soft_spearman_loss(preds, theory_vals, temperature=1.0):
    """
    Differentiable negative Spearman rank correlation loss.
    Computes soft ranks via sigmoid pairwise comparisons, then Pearson
    correlation of soft ranks = differentiable Spearman approximation.
    Minimizing this maximizes theory rank alignment.
    Applied to primal training rows only.
    """
    n = preds.shape[0]
    if n < 3:
        return torch.tensor(0.0, device=preds.device)
    # Soft rank: sum of sigmoid(pred_i - pred_j) over j ≠ i → smooth rank proxy
    pred_diff = (preds.unsqueeze(0) - preds.unsqueeze(1)) / temperature  # [n, n]
    pred_soft_rank = torch.sigmoid(pred_diff).sum(1)  # [n]
    theory_diff = (theory_vals.unsqueeze(0) - theory_vals.unsqueeze(1)) / temperature
    theory_soft_rank = torch.sigmoid(theory_diff).sum(1)  # [n]
    # Pearson between soft ranks ≈ Spearman
    p = pred_soft_rank - pred_soft_rank.mean()
    t = theory_soft_rank - theory_soft_rank.mean()
    corr = (p * t).sum() / (torch.sqrt((p**2).sum() * (t**2).sum()) + 1e-8)
    return -corr  # negate: minimize = maximize correlation


def fit_neural_model(X_tr, y_tr, X_val, y_val, epochs=500, lr=1e-3, hidden=(64, 32),
                     rank_lambda=0.0, theory_tr=None, primal_mask_tr=None):
    mu = X_tr.mean(axis=0)
    std = X_tr.std(axis=0) + 1e-8
    X_tr_n = (X_tr - mu) / std
    X_val_n = (X_val - mu) / std

    X_t = torch.tensor(X_tr_n, dtype=torch.float32).to(device)
    y_t = torch.tensor(y_tr, dtype=torch.float32).to(device)
    X_v = torch.tensor(X_val_n, dtype=torch.float32).to(device)

    # Prepare ranking inputs (primal training rows only)
    primal_idx_t = None
    theory_t = None
    if rank_lambda > 0 and theory_tr is not None and primal_mask_tr is not None:
        pidx = np.where(primal_mask_tr)[0]
        if len(pidx) > 10:
            primal_idx_t = torch.tensor(pidx, dtype=torch.long).to(device)
            theory_t = torch.tensor(theory_tr[pidx], dtype=torch.float32).to(device)

    model = CostMLP(X_tr.shape[1], hidden=hidden).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    best_val_loss = float("inf")
    best_state = None

    for epoch in range(epochs):
        model.train()
        pred = model(X_t)
        mse_loss = nn.MSELoss()(pred, y_t)
        if rank_lambda > 0 and primal_idx_t is not None:
            rank_loss = soft_spearman_loss(pred[primal_idx_t], theory_t)
            loss = mse_loss + rank_lambda * rank_loss
        else:
            loss = mse_loss
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()
        scheduler.step()

        if epoch % 50 == 0 or epoch == epochs - 1:
            model.eval()
            with torch.no_grad():
                val_pred = model(X_v).cpu().numpy()
                val_loss = np.mean((y_val - val_pred) ** 2)
            if val_loss < best_val_loss:
                best_val_loss = val_loss
                best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            model.train()

    if best_state:
        model.load_state_dict(best_state)
    model.eval()

    def predict_fn(X_new):
        X_new_n = (X_new - mu) / std
        with torch.no_grad():
            return model(torch.tensor(X_new_n, dtype=torch.float32).to(device)).cpu().numpy()

    return predict_fn, model, mu, std



