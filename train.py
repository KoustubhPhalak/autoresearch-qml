# ─────────────────────────────────────────────
def extrapolate_to_nist(predict_fn, nist_params, rows,
                        betas=[35, 60, 80, 100, 150, 200]):
    """
    NIST PQC parameter assessment — two separate analyses with distinct roles.

    Part A — RUNTIME EXTRAPOLATION (uses the fitted cost model)
      Predicts log₂(seconds) for BKZ-β at NIST-scale lattices.
      This is what the agent improves via PRED_SCORE. Machine-specific
      but gives an operational sense of feasibility.
      Units: log₂(seconds). NOT security bits.

    Part B — δ-BASED SECURITY ESTIMATE (uses raw benchmark δ(β), NOT the model)
      Fits δ(β) from empirical root-Hermite factors, extrapolates to find
      the β where δ is small enough to solve NIST instances.
      Then: classical bits = 0.292 × β*, quantum bits = 0.265 × β*.

      This IS in correct units (dimensionless security bits), but is a
      heuristic extrapolation with significant caveats:
        - δ measured at small β (≤40), extrapolated to β ~ 400–700
        - Polynomial BKZ overhead not captured in the δ model
        - Module-LWE structure not modeled (plain-LWE proxy)
        - Verdicts are exploratory, not validated security claims

    Future autoresearch direction: the agent could learn to predict δ(β)
    directly (not just runtime), which would tie Parts A and B together.
    """
    from collections import defaultdict

    print("\n" + "=" * 70)
    print("NIST PQC PARAMETER ASSESSMENT")
    print("(plain-LWE proxy — Module-LWE structure not modeled)")
    print("=" * 70)

    # ────────────────────────────────────────────────────────────
    # Part A: Runtime extrapolation using the fitted cost model
    # ────────────────────────────────────────────────────────────
    print("\n  PART A: Predicted BKZ runtimes at NIST scale")
    print("  (Uses the fitted cost model — improving PRED_SCORE improves these)")
    print("  (Units: log₂(seconds) — machine-specific, NOT security bits)")
    print("  " + "─" * 55)

    for name, params in nist_params.items():
        n = params["n"]
        q = params["q"]
        eta = params["eta1"]
        sigma = math.sqrt(eta / 2)
        claimed = params["claimed_bits"]
        m = n
        d = m + n + 1

        print(f"\n  {name}: n={n}, q={q}, claimed={claimed}-bit")

        for attack_label, d_val, is_dual in [("primal", d, 0.0), ("dual", m, 1.0)]:
            print(f"    [{attack_label:6s}]  ", end="")
            parts = []
            for beta in betas:
                if beta >= d_val:
                    continue
                theory_approx = compute_theory_log2(n, q, sigma, d_val)
                feat = np.array([
                    math.log2(n), math.log2(q), math.log2(max(sigma, 0.1)),
                    beta, d_val, math.log2(d_val), beta / d_val,
                    n * math.log2(q), beta * math.log2(d_val),
                    is_dual,
                    math.log2(beta),
                    math.log2(d_val) - math.log2(beta),
                    beta * math.log2(beta),
                    theory_approx,
                ]).reshape(1, -1)
                try:
                    log2_sec = float(predict_fn(feat)[0])
                    parts.append(f"β={beta}:{log2_sec:+.1f}")
                except Exception:
                    pass
            print("  ".join(parts) if parts else "no predictions")

    # ────────────────────────────────────────────────────────────
    # Part B: δ-based security estimates from raw benchmark data
    # ────────────────────────────────────────────────────────────
    print(f"\n\n  PART B: δ-based security estimates (HEURISTIC)")
    print("  (Uses raw δ(β) data, NOT the fitted cost model)")
    print("  " + "─" * 55)

    # Collect empirical δ(β) from primal benchmarks
    delta_by_beta = defaultdict(list)
    for row in rows:
        if row["attack"] != "primal":
            continue
        if row["root_hermite_factor"] > 0:
            delta_by_beta[row["beta"]].append(row["root_hermite_factor"])

    if not delta_by_beta:
        print("  No δ data available.")
        return []

    beta_vals = sorted(delta_by_beta.keys())
    delta_medians = [float(np.median(delta_by_beta[b])) for b in beta_vals]

    print(f"\n  Empirical δ(β) from primal benchmarks:")
    for b, d_med in zip(beta_vals, delta_medians):
        print(f"    β={b:3d}:  median δ = {d_med:.6f}")

    if len(beta_vals) < 3:
        print("  Too few β values to fit δ model.")
        return []

    # Fit δ(β) model: log(δ) ≈ a/β + b
    # As β → ∞, δ → exp(b) (asymptotic Hermite factor)
    inv_betas = np.array([1.0 / b for b in beta_vals])
    log_deltas = np.array([math.log(d) for d in delta_medians])
    coeffs = np.polyfit(inv_betas, log_deltas, 1)
    a_coeff, b_coeff = coeffs[0], coeffs[1]

    # Show fit quality
    delta_at_inf = math.exp(b_coeff)
    print(f"\n  Fitted model: log(δ) ≈ {a_coeff:.4f}/β + {b_coeff:.6f}")
    print(f"  Asymptotic δ (β→∞): {delta_at_inf:.6f}")
    print(f"  Fit residuals:")
    for b, d_med in zip(beta_vals, delta_medians):
        predicted = math.exp(a_coeff / b + b_coeff)
        print(f"    β={b:3d}:  actual={d_med:.6f}  predicted={predicted:.6f}  "
              f"err={d_med - predicted:+.6f}")

    # For each NIST scheme, find β* where δ^d · vol^{1/d} ≤ target_norm
    print(f"\n  Security estimates:")
    print(f"  {'─' * 65}")

    assessment_rows = []

    for name, params in nist_params.items():
        n = params["n"]
        q = params["q"]
        eta = params["eta1"]
        sigma = math.sqrt(eta / 2)
        claimed = params["claimed_bits"]
        m_p = n
        d_p = m_p + n + 1

        target_norm = sigma * math.sqrt(m_p + n)
        log_vol = m_p * math.log(q)
        log_target = math.log(max(target_norm, 1e-10))

        # Solve: d * log(δ(β)) + log_vol/d ≤ log(target_norm)
        # d * (a/β + b) ≤ log_target - log_vol/d
        rhs = log_target - log_vol / d_p
        # d*a/β ≤ rhs - d*b
        denom = rhs - d_p * b_coeff

        if denom > 0 and a_coeff * d_p > 0:
            beta_emp = int(math.ceil(a_coeff * d_p / denom))
        else:
            beta_emp = None

        if beta_emp is None or beta_emp < 2 or beta_emp > 100000:
            print(f"\n  {name}: β* estimation failed "
                  f"(δ model doesn't converge to target)")
            continue

        cl_bits = 0.292 * beta_emp
        qu_bits = 0.265 * beta_emp

        print(f"\n  {name} (n={n}, claimed={claimed}-bit):")
        print(f"    Empirical β* ≈ {beta_emp}  "
              f"(extrapolated from δ at β ≤ {max(beta_vals)})")
        print(f"    ┌─ Classical security (0.292 × β*): {cl_bits:.1f} bits  "
              f"[Δ = {cl_bits - claimed:+.1f}]")
        print(f"    └─ Quantum security   (0.265 × β*): {qu_bits:.1f} bits  "
              f"[Δ = {qu_bits - claimed:+.1f}]")

        for threat, bits in [("classical", cl_bits), ("quantum", qu_bits)]:
            delta_bits = bits - claimed
            if delta_bits > 10:
                verdict = "OVER-CONSERVATIVE"
            elif delta_bits > 2:
                verdict = "slightly over-conservative"
            elif delta_bits > -2:
                verdict = "CONSISTENT"
            elif delta_bits > -10:
                verdict = "slightly under-conservative"
            else:
                verdict = "UNDER-CONSERVATIVE"
            assessment_rows.append({
                "scheme": name, "threat_model": threat,
                "claimed_bits": claimed, "beta_empirical": beta_emp,
                "security_bits": bits, "delta_bits": delta_bits,
                "verdict": verdict,
            })

    # Summary
    if assessment_rows:
        print(f"\n  {'=' * 65}")
        print(f"  SUMMARY (HEURISTIC — extrapolated from β ≤ {max(beta_vals)})")
        print(f"  {'=' * 65}")
        print(f"  {'Scheme':<12s} {'Threat':<12s} {'β*':>5s} {'Claimed':>7s} "
              f"{'Est.':>6s} {'Δ':>6s}  Verdict")
        print(f"  {'─'*12} {'─'*12} {'─'*5} {'─'*7} {'─'*6} {'─'*6}  {'─'*25}")
        for r in assessment_rows:
            print(f"  {r['scheme']:<12s} {r['threat_model']:<12s} "
                  f"{r['beta_empirical']:>5d} {r['claimed_bits']:>7d} "
                  f"{r['security_bits']:>6.1f} {r['delta_bits']:>+6.1f}  "
                  f"{r['verdict']}")

        print(f"\n  CAVEATS:")
        print(f"  • β* extrapolated from δ measured at β ≤ {max(beta_vals)}")
        print(f"  • BKZ polynomial overhead not captured")
        print(f"  • Module-LWE → plain-LWE approximation")
        print(f"  • These are HEURISTIC exploratory estimates, not validated claims")

    return assessment_rows



