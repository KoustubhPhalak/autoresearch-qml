def hermite_factor(beta):
    """
    BKZ root-Hermite factor — leading term (prepare.py convention).
    δ = (β/(2πe))^{1/(2(β-1))} — omitting the (πβ)^{1/β} refinement
    so that compute_theory_log2 produces the same beta_est as prepare.py.
    """
    if beta <= 1:
        return 1.5
    log_delta = math.log(beta / (2 * math.pi * math.e)) / (2 * (beta - 1))
    return math.exp(log_delta)


def compute_theory_log2(n, q, sigma_eff, d):
    """
    Estimate theory_classical_log2 using Core-SVP + GSA uSVP condition.
    Returns 0.292 * beta_star where beta_star is the minimum block size
    satisfying the Gaussian heuristic for primal uSVP attack.

    Uses the same hermite_factor formula and optimal_m as prepare.py:
      m = max(n, min(2n, ceil(n*log(q)/log(max(q/2,2)))))
      d = m + n + 1 (primal lattice dimension)
      δ = (β/(2πe))^{1/(2(β-1))}
      condition: d*log(δ) + log_vol/d ≤ log(σ√(m+n))
    """
    # match prepare.py's optimal_m
    m = max(n, min(2 * n, int(math.ceil(n * math.log(q) / math.log(max(q / 2, 2))))))
    target_norm = max(sigma_eff, 0.1) * math.sqrt(n + m)
    log_vol = m * math.log(q)
    log_target = math.log(max(target_norm, 1e-10))

    best_beta = d  # fallback: full dimension
    for b in range(5, d + 1):
        if b <= 1:
            continue
        log_delta = math.log(b / (2 * math.pi * math.e)) / (2 * (b - 1))
        log_predicted = d * log_delta + log_vol / d
        if log_predicted <= log_target:
            best_beta = b
            break

    return 0.292 * best_beta


def extract_features(row):
    """
    Extract features from a single data row.
    Returns: feature vector, log2(wall_time) target.

    All features must be computable at NIST-extrapolation time
    (i.e., no realized error_norm or other instance-specific quantities
    that would not be known for a hypothetical target instance).

    Features:
      0: log2(n)               — LWE dimension
      1: log2(q)               — modulus
      2: log2(sigma_eff)       — effective noise parameter
      3: beta                  — BKZ block size
      4: d                     — lattice dimension
      5: log2(d)               — log lattice dimension
      6: beta / d              — relative block size
      7: n * log2(q)           — volume proxy
      8: beta * log2(d)        — interaction: block size × lattice complexity
      9: is_dual               — 1.0 if dual attack, 0.0 if primal
     10: log2(beta)            — BKZ overhead term
     11: log2(d) - log2(beta)  — log(d/β): BKZ rounds
     12: beta * log2(beta)     — β·log(β): Stirling correction
     13: theory_classical_log2 — RADICAL: direct Core-SVP theory prediction
    """
    n = row["n"]
    q = row["q"]
    sigma = max(row["sigma_eff"], 0.1)
    beta = row["beta"]
    d = row["d"]
    is_dual = 1.0 if row["attack"] == "dual" else 0.0
    theory = row["theory_classical_log2"]

    features = [
        math.log2(n),
        math.log2(q),
        math.log2(sigma),
        beta,
        d,
        math.log2(d),
        beta / d,
        n * math.log2(q),
        beta * math.log2(d),
        is_dual,
        math.log2(beta),                       # 10: explicit log(β)
        math.log2(d) - math.log2(beta),         # 11: log(d/β) BKZ rounds
        beta * math.log2(beta),                 # 12: β·log(β) Stirling
        theory,                                 # 13: theory_classical_log2 DIRECT
    ]

    target = math.log2(max(row["wall_time"], 1e-6))
    return np.array(features, dtype=np.float64), target


def build_dataset(rows):
    """Build feature matrix X, target vector y, and metadata from flat rows."""
    X_list, y_list, meta_list = [], [], []

    for row in rows:
        if row["wall_time"] <= 0 or row["root_hermite_factor"] <= 0:
            continue
        feat, target = extract_features(row)
        X_list.append(feat)
        y_list.append(target)
        meta_list.append({
            "n": row["n"],
            "is_extrap": row["is_extrap"],
            "beta": row["beta"],
            "attack": row["attack"],
            "instance_group": row["instance_group"],
            "theory_classical_log2": row["theory_classical_log2"],
            "wall_time": row["wall_time"],
        })

    return np.array(X_list), np.array(y_list), meta_list


