# Polynomial Feature Expansion
# ─────────────────────────────────────────────
def poly_features(X, degree=2):
    """
    Add polynomial and interaction features up to given degree.
    For degree=2: adds squares of each feature + pairwise products.
    Keeps is_dual binary (no squaring needed for indicator).
    """
    n, d = X.shape
    feats = [X]
    # Squares of continuous features (skip is_dual = col 9)
    sq = X[:, :9] ** 2
    feats.append(sq)
    # Pairwise products of most informative features
    # Exclude theory_classical_log2 [13] — only 5 distinct values → poly cross-terms overfit
    # Exclude log2(β) [10], log(d/β) [11], β·log(β) [12] — β cross-terms hurt theory_corr
    key_cols = [0, 1, 3, 5, 6, 7, 8]
    for i in range(len(key_cols)):
        for j in range(i+1, len(key_cols)):
            ci, cj = key_cols[i], key_cols[j]
            feats.append((X[:, ci] * X[:, cj]).reshape(-1, 1))
    return np.hstack(feats)


