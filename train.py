def r_squared(y_true, y_pred):
    ss_res = np.sum((y_true - y_pred) ** 2)
    ss_tot = np.sum((y_true - y_true.mean()) ** 2)
    if ss_tot < 1e-12:
        return 0.0
    return 1.0 - ss_res / ss_tot


def spearman_rank_corr(x, y):
    from scipy.stats import spearmanr
    if len(x) < 3:
        return 0.0
    corr, _ = spearmanr(x, y)
    return corr if not math.isnan(corr) else 0.0


def compute_pred_score(y_interp_true, y_interp_pred,
                       y_extrap_true, y_extrap_pred,
                       theory_preds_primal, y_extrap_primal_preds):
    """
    Composite PRED_SCORE:
      0.3 * R²_interp + 0.5 * R²_extrap + 0.2 * theory_rank_correlation

    theory_rank_correlation uses PRIMAL extrapolation rows only,
    because the theory model is a primal uSVP proxy.
    Each component clipped to [0, 1].
    """
    r2_interp = max(0.0, r_squared(y_interp_true, y_interp_pred))
    r2_extrap = max(0.0, r_squared(y_extrap_true, y_extrap_pred)) if len(y_extrap_true) > 0 else 0.0

    if len(theory_preds_primal) > 2 and len(y_extrap_primal_preds) > 2:
        theory_corr = max(0.0, spearman_rank_corr(theory_preds_primal, y_extrap_primal_preds))
    else:
        theory_corr = 0.0

    score = 0.3 * r2_interp + 0.5 * r2_extrap + 0.2 * theory_corr
    return score, r2_interp, r2_extrap, theory_corr


