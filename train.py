# Baseline Model: Linear Regression
# ─────────────────────────────────────────────
def fit_linear_model(X_tr, y_tr, lam=1e-4):
    n, d = X_tr.shape
    X_aug = np.column_stack([X_tr, np.ones(n)])
    w = np.linalg.solve(X_aug.T @ X_aug + lam * np.eye(d + 1), X_aug.T @ y_tr)
    return w

def predict_linear(X, w):
    X_aug = np.column_stack([X, np.ones(X.shape[0])])
    return X_aug @ w


